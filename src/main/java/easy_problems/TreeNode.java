package easy_problems;

public class TreeNode {

	int val;
	TreeNode left;
	TreeNode right;

	TreeNode() {
		
	}
	
	TreeNode(int val) {
		this.val = val;
	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

	public TreeNode getLeft() {
		return left;
	}

	public void setLeft(TreeNode val) {
		this.left = val;
	}

	public TreeNode getRight() {
		return right;
	}

	public void setRight(TreeNode right) {
		this.right = right;
	}

}
